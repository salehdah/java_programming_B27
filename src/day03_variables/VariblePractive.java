package day03_variables;

public class VariblePractive {
    public static void main(String[] args) {
        byte age; // i declared a variable called age
        age=10; //assigning 10 to the age variable
        System.out.println(age);//printing the value of the variable
        System.out.println("age");// printing the character age not its value
        System.out.println("20");//printing the character 2 and 0
        System.out.println(20);//printing the int 20
        //by default 20 is int type
        System.out.println(age +1);


        short num=200;//declared and assigned at the same time
    }
}
