package day06_operators;

public class incrementDecrement {
    public static void main(String[] args) {
        int b=25;
        System.out.println(++b);
        b=23;
        System.out.println(b++);
        System.out.println(b);
        int x=14;
        x++;
        System.out.println(x++);
        System.out.println(x);





    }
}
